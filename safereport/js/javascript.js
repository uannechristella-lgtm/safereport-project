function translatePage(lang) {
    var elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(function(element) {
        var key = element.dataset.i18n;
        if (!key || !translations[lang] || translations[lang][key] === undefined) {
            return;
        }
        var translationValue = translations[lang][key];
        if (element.tagName === 'INPUT' && (element.type === 'submit' || element.type === 'reset' || element.type === 'button')) {
            element.value = translationValue;
        } else {
            element.innerHTML = translationValue;
        }
    });

    var placeholderElements = document.querySelectorAll('[data-i18n-placeholder]');
    placeholderElements.forEach(function(element) {
        var key = element.dataset.i18nPlaceholder;
        if (!key || !translations[lang] || translations[lang][key] === undefined) {
            return;
        }
        element.placeholder = translations[lang][key];
    });

    document.documentElement.lang = lang;
    localStorage.setItem('safereportLanguage', lang);
}

function setLanguage(lang) {
    if (translations[lang]) {
        currentLanguage = lang;
        translatePage(lang);
    }
}

function loadLanguage() {
    var storedLang = localStorage.getItem('safereportLanguage') || 'en';
    setLanguage(storedLang);
}

document.addEventListener('DOMContentLoaded', loadLanguage);



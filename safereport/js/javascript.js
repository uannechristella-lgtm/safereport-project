function toggleName()
{
    var radios = document.forms["reportform"].reportType;
    var nameField = document.getElementById("nameFieldGroup");

    if (radios[1].checked)
    {
        nameField.style.display = "block";
    }
    else
    {
        nameField.style.display = "none";
    }
}

function validate_required(field, alerttxt)
{
    with (field)
    {
        if (value == null || value == "")
        {
            alert(alerttxt);
            return false;
        }
        else
        {
            return true;
        }
    }
}

function validate_email(field, alerttxt)
{
    with (field)
    {
        if (value == "")
        {
            return true;
        }
        apos = value.indexOf("@");
        dotpos = value.lastIndexOf(".");
        if (apos < 1 || dotpos - apos < 2)
        {
            alert(alerttxt);
            return false;
        }
        else
        {
            return true;
        }
    }
}

function validate_form(thisform)
{
    with (thisform)
    {
        if (validate_required(description, "Please describe what happened before submitting.") == false)
        {
            description.focus();
            return false;
        }
        if (validate_email(email, "Please enter a valid email address, or leave it blank.") == false)
        {
            email.focus();
            return false;
        }
    }

    saveReport(thisform);
    return true;
}


// ===================== CASE TRACKING =====================

function generateCaseCode()
{
    return "SR-" + new Date().getFullYear() + "-" + Math.floor(1000 + Math.random() * 9000);
}

function saveReport(thisform)
{
    var code = generateCaseCode();
    var report = {
        code: code,
        reportType: thisform.reportType.value,
        category: thisform.category.value,
        residence: thisform.residence.value,
        location: thisform.location.value,
        description: thisform.description.value,
        status: "Submitted",
        dateSubmitted: new Date().toLocaleDateString()
    };

    var reports = JSON.parse(localStorage.getItem("safereportReports")) || [];
    reports.push(report);
    localStorage.setItem("safereportReports", JSON.stringify(reports));

    document.getElementById("codeField").value = code;
}


